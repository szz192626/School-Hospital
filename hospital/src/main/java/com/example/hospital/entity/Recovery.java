package com.example.hospital.entity;

public class Recovery {
    private Integer recoveryid ;
    private String recoveryname ;
    private Integer recoverynumber ;
    private String  recoverypihao ;
    private String   jbr ;
    private String  beizhu ;

    public Integer getRecoveryid() {
        return recoveryid;
    }

    public void setRecoveryid(Integer recoveryid) {
        this.recoveryid = recoveryid;
    }

    public String getRecoveryname() {
        return recoveryname;
    }

    public void setRecoveryname(String recoveryname) {
        this.recoveryname = recoveryname;
    }

    public Integer getRecoverynumber() {
        return recoverynumber;
    }

    public void setRecoverynumber(Integer recoverynumber) {
        this.recoverynumber = recoverynumber;
    }

    public String getRecoverypihao() {
        return recoverypihao;
    }

    public void setRecoverypihao(String recoverypihao) {
        this.recoverypihao = recoverypihao;
    }

    public String getJbr() {
        return jbr;
    }

    public void setJbr(String jbr) {
        this.jbr = jbr;
    }

    public String getBeizhu() {
        return beizhu;
    }

    public void setBeizhu(String beizhu) {
        this.beizhu = beizhu;
    }
}
