package com.example.hospital.entity;

public class Lack {
    private Integer lackId;
    private String lackName;
    private Integer lackNum;

    public Integer getLackId() {
        return lackId;
    }

    public void setLackId(Integer lackId) {
        this.lackId = lackId;
    }

    public String getLackName() {
        return lackName;
    }

    public void setLackName(String lackName) {
        this.lackName = lackName;
    }

    public Integer getLackNum() {
        return lackNum;
    }

    public void setLackNum(Integer lackNum) {
        this.lackNum = lackNum;
    }
}
